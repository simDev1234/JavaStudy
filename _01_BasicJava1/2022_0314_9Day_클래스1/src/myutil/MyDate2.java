package myutil;

public class MyDate2 {
	private int year;
	private int month;
	private int day;
	
	//getter, setter 자동 생성 가능
		//Alt+shift+s 눌러서 코드 자동완성
		//또는, [source] > [Generate Getters and Setters]
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}	
}
